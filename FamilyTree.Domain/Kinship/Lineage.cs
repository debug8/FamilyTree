namespace FamilyTree.Domain.Kinship;

/// <summary>
/// Лінія спорідненості — через кого з батьків кореневої особи йде зв'язок.
/// Має сенс переважно для бічних зв'язків старшої гілки (тітки/дядьки, двоюрідні діди)
/// та кузенів. Для прямих предків/нащадків і сиблінгів — <see cref="Unknown"/>.
/// </summary>
public enum Lineage
{
    /// <summary>Лінію не визначено (або вона неоднозначна для цього зв'язку).</summary>
    Unknown,

    /// <summary>По батьковій лінії.</summary>
    Paternal,

    /// <summary>По материній лінії.</summary>
    Maternal,

    /// <summary>Зв'язок іде через обох батьків (напр. рідні сиблінги).</summary>
    Mixed,
}
